public class Loops{
    public static void main(Sting[] args){
        int x = 0;
        while(x < 10){
            System.out.println(x);
            x++;
        }

        int y = 0;
        do{
            System.out.println(x);
            y++;
        }while(y < 10);

        for(int z = 0; z < 10; z++){
            System.out.println(z);
        }
    }
}
